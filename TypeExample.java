public class StringPlusExample {
    public static void main(String[] args) {
        int a = 5;
        double b = 2.5;
        double result = a + b; // результат типа double
        System.out.println("a + b = " + result);
        
        float f = 4.5f;
        long l = 100L;
        float result2 = f + l; // result3 — float
        System.out.println("f + l = " + result2);
    }
}