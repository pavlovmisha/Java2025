/**
 * Класс, представляющий простого человека.
 */
public class Person {
    
    /**
     * Имя человека.
     */
    private String name;

    /**
     * Конструктор.
     * @param name имя
     */
    public Person(String name) {
        this.name = name;
    }
    
    /**
     * Получить имя.
     * @return имя
     */
    public String getName() {
        return name;
    }
}



