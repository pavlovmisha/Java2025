enum Day {
    ПОНЕДЕЛЬНИК("Будний день"),
    ВТОРНИК("Будний день"),
    СРЕДА("Будний день"),
    ЧЕТВЕРГ("Будний день"),
    ПЯТНИЦА("Будний день"),
    СУББОТА("Выходной"),
    ВОСКРЕСЕНИЕ("Выходной");

    // Поле для хранения описания дня
    private String описание;

    // Конструктор enum, вызываемый при создании элементов
    Day(String описание) {
        this.описание = описание;
    }

    // Метод для получения описания
    public String getОписание() {
        return описание;
    }
}

public class Main {
    public static void main(String[] args) {
        for (Day день : Day.values()) {
            System.out.println(день + " — " + день.getОписание());
        }
    }
}